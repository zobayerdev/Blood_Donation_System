private void cap() {
        final String alphabet = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz";
        final int N = alphabet.length();
        Random r = new Random();
        String text = "";
        for (int i = 0; i < 5; i++) {
            text += String.valueOf(alphabet.charAt(r.nextInt(N)));
        }
        jLabel2.setText(String.valueOf(text));
    }
